#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	j;
	int	alpha[256] = {0, };

	i = 0;
	j = 0;
	if (ac == 3)
	{
		while (av[1][i])
		{
			j = 0;
			while (av[2][j])
			{
				if (alpha[(int)av[1][i]] == 0 && av[1][i] == av[2][j])
				{
					write(1, &av[1][i], 1);
					alpha[(int)av[1][i]] = 1;
				}
				j++;
			}
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
